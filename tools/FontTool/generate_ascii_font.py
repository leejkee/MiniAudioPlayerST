def main():
    parser = argparse.ArgumentParser(
        description="Generate SSD1315 ASCII 8x16 font C arrays (embedded bitmap, zero deps)"
    )
    parser.add_argument(
        "--first", type=lambda x: int(x, 0), default=0x20,
        help="First char code (default: 0x20)"
    )
    parser.add_argument(
        "--last", type=lambda x: int(x, 0), default=0x7E,
        help="Last char code (default: 0x7E)"
    )
    parser.add_argument(
        "--char-w", type=int, default=8, help="Char cell width (default: 8)"
    )
    parser.add_argument(
        "--char-h", type=int, default=16, help="Char cell height, multiple of 8 (default: 16)"
    )
    parser.add_argument(
        "--out-dir", default=None,
        help="Output dir (default: ../firmware/MiniAudioPlayerST/App)"
    )
    parser.add_argument(
        "--preview", action="store_true",
        help="Write preview.txt with ASCII-art glyph map"
    )
    args = parser.parse_args()

    if args.char_h % 8 != 0:
        print("ERROR: char_h must be a multiple of 8")
        sys.exit(1)

    pages = args.char_h // 8
    count = args.last - args.first + 1
    data_size = count * pages * args.char_w

    # Output dirs
    script_dir = os.path.dirname(os.path.abspath(__file__))
    out_dir = args.out_dir or os.path.join(
        script_dir, "..", "firmware", "MiniAudioPlayerST", "App"
    )
    out_dir = os.path.normpath(out_dir)
    include_dir = os.path.join(out_dir, "include")
    src_dir = os.path.join(out_dir, "src")
    os.makedirs(include_dir, exist_ok=True)
    os.makedirs(src_dir, exist_ok=True)

    print(f"Font:   embedded VGA 8x16 console bitmap")
    print(f"Cell:   {args.char_w}×{args.char_h} ({pages} page(s))")
    print(f"Range:  U+{args.first:04X}–U+{args.last:04X} ({count} glyphs)")
    print(f"Data:   {data_size} bytes")
    print(f"Output: {out_dir}")
    print()

    # Header
    h_path = os.path.join(include_dir, "font_en.h")
    with open(h_path, "w", encoding="utf-8", newline="\n") as f:
        f.write(generate_header(args.first, args.last, args.char_w, args.char_h))
    print(f"[OK] {h_path}")

    # Source
    c_path = os.path.join(src_dir, "font_en.c")
    with open(c_path, "w", encoding="utf-8", newline="\n") as f:
        f.write(generate_source(args.first, args.last, args.char_w, args.char_h))
    print(f"[OK] {c_path}")

    # Preview
    if args.preview:
        preview_path = os.path.join(out_dir, "font_preview.txt")
        write_preview_txt(preview_path, args.first, args.last, args.char_w, args.char_h)

    print()
    print("Done. Zero dependencies, deterministic output.")


if __name__ == "__main__":
    main()
