def generate_header(first, last, char_w, char_h):
    pages = char_h // 8
    count = last - first + 1
    return f"""#ifndef __FONT_EN_H__
#define __FONT_EN_H__

#ifdef __cplusplus
extern "C" {{
#endif

#include <stdint.h>

/* {char_w}x{char_h} ASCII font — standard VGA 8x16 console bitmap
 * {count} glyphs (U+{first:04X} to U+{last:04X}), {count * pages * char_w} bytes total
 * Format: column-major per GRAM page, bit 0 = top row of page
 */
#define FONT_EN_CHAR_W    {char_w}
#define FONT_EN_CHAR_H    {char_h}
#define FONT_EN_FIRST     0x{first:02X}
#define FONT_EN_LAST      0x{last:02X}
#define FONT_EN_COUNT     {count}

extern const uint8_t font_en_8x16[FONT_EN_COUNT][{pages * char_w}];

#ifdef __cplusplus
}}
#endif

#endif /* __FONT_EN_H__ */
"""


def generate_source(first, last, char_w, char_h):
    pages = char_h // 8
    count = last - first + 1
    bytes_per_glyph = pages * char_w

    lines = []
    lines.append('#include "font_en.h"')
    lines.append("")
    lines.append("/* Standard VGA 8x16 console font")
    lines.append(f"   Glyphs: U+{first:04X}–U+{last:04X} ({count} chars)")
    lines.append(f"   Format: column-major, {pages} page(s) × {char_w} columns")
    lines.append(f"   Total:  {count * bytes_per_glyph} bytes")
    lines.append("*/")
    lines.append(
        f"const uint8_t font_en_8x16[FONT_EN_COUNT][{bytes_per_glyph}] = {{"
    )

    for code in range(first, last + 1):
        bitmap = get_glyph(code, first)
        char_name = chr(code) if 0x20 <= code < 0x7F else f"\\x{code:02X}"
        lines.append(f"    /* U+{code:04X} '{char_name}' */")

        # Flatten all pages into a single {byte0, ..., byteN} initializer
        all_bytes = ", ".join(f"0x{b:02X}" for b in bitmap)
        lines.append(f"    {{{all_bytes}}},")

    lines.append("};")
    return "\n".join(lines) + "\n"


def write_preview_txt(path, first, last, char_w=8, char_h=16):
    """Write an ASCII-art preview of all glyphs to a text file (no deps)."""
    count = last - first + 1
    chars_per_row = 16
    glyphs = [get_glyph(c, first) for c in range(first, last + 1)]
    pages = char_h // 8

    with open(path, "w", encoding="utf-8") as f:
        f.write(f"Font preview: U+{first:04X}–U+{last:04X} ({count} glyphs)\n")
        f.write(f"Cell: {char_w}×{char_h}, {'█' if True else ''}=on  {'·' if True else ''}=off\n\n")

        for block_start in range(0, count, chars_per_row):
            block_chars = glyphs[block_start:block_start + chars_per_row]
            block_n = len(block_chars)

            # Header row: character codes
            for i in range(block_n):
                code = first + block_start + i
                f.write(f"  U+{code:04X}  ")
            f.write("\n")

            # Character labels
            for i in range(block_n):
                code = first + block_start + i
                label = chr(code) if code != 0x20 else "SP"
                f.write(f"   '{label}'   ")
            f.write("\n")

            # Pixel rows
            for row in range(char_h):
                for i in range(block_n):
                    bitmap = block_chars[i]
                    page = row // 8
                    bit = row % 8
                    for col in range(char_w):
                        byte_val = bitmap[page * char_w + col]
                        on = (byte_val >> bit) & 1
                        f.write("█" if on else "·")
                    f.write("  ")
                f.write("\n")
            f.write("\n")

    print(f"[OK] Preview: {path}")


